def existing_names() -> set[str]:
    names: set[str] = set()
    for path in (RAW, EXTRA, OUT):
        if not path.exists():
            continue
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
        except Exception:
            continue
        for h in data:
            if h.get("name_zh"):
                names.add(h["name_zh"])
            if h.get("key"):
                names.add(h["key"])
    if DB.exists():
        con = sqlite3.connect(DB)
        try:
            for (n,) in con.execute("SELECT name_zh FROM herbs"):
                if n:
                    names.add(n)
        finally:
            con.close()
    return names


